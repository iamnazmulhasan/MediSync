def main():
    # Deduplicate while preserving order
    seen = set()
    names = []
    for line in RAW_DATA.splitlines():
        name = line.strip()
        if not name:
            continue
        if name in seen:
            continue
        seen.add(name)
        names.append(name)

    fixture = [{"model": MODEL, "fields": {"name": n}} for n in names]

    OUTPUT_FILE.write_text(
        json.dumps(fixture, indent=2, ensure_ascii=False),
        encoding="utf-8"
    )

    print(f"Wrote {len(fixture)} records to: {OUTPUT_FILE.resolve()}")


if __name__ == "__main__":
    main()