// Bonus: Search helper (perfect for autocomplete input)
export const searchSymptoms = (query = "") => {
  if (!query || query.trim() === "") {
    return commonSymptoms.slice(0, 50); // show popular ones first
  }
  
  const lowerQuery = query.toLowerCase().trim();
  
  return commonSymptoms
    .filter(symptom =>
      symptom.toLowerCase().includes(lowerQuery)
    )
    .slice(0, 30); // limit results for performance
};

